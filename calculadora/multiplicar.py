def multiplicar(
    n1:float,
    n2:float
) -> float:
    """
    Función que multiplíca 2 números

    Args:
        n1 (float): factor 1
        n2 (float): factor 2

    Returns:
        float: Resultado de multiplicar los 2 números 
    resultado = n1 * n2
    return resultado
    """
    resultado = n1 * n2
    return resultado