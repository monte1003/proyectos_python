def sumar(
    n1: float,
    n2: float
) -> float:
    """
    Función que consiste en sumar 2 números

    ----Argumentos----:
        n1 (float): Sumando 1
        n2 (float): Sumando 2

    ----Return----:
        float: Resultado de la suma de los 2 números
    """
    resultado = n1 + n2
    return resultado