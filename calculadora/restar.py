def restar(
    n1: float,
    n2: float
) -> float:
    """
    Función que consiste en restar 2 números

    ----Argumentos----:
        n1 (float): Miniuendo
        n2 (float): Sustraendo

    ----Return----:
        float: Resultado de restar los 2 números
    """
    resultado = n1 - n2
    return resultado
