import math
def raiz_cuadrada(
    n1:float,
    n2:float
) -> float:
    """
    Función que saca la raíz cuadrado de un número 
    
    ----Argumentos----:
        n1 (float): numero 1
        n2 (float): numero 2

    Returns:
        float: Resultado
    """
    result = math.sqrt(n1,n2)
    return result